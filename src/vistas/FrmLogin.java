    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
         try {
            String username = txtUsuario.getText();
            String password = psfPassword.getText();
            if (username.equals("admin") && password.equals("123")) {
                role = "Administrador";
                userID = Long.parseLong("0");
            } else {
                Login login = controlador.iniciarSesion(username, password);
                userID = login.getDni();
                role = login.getRol();
            }
            MenuPrincipal mainMenu = new MenuPrincipal();
            mainMenu.setVisible(true);
            this.setVisible(false);

        } catch (ConexionException | PasswordIncorrectoException | UsernameIncorrectoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

