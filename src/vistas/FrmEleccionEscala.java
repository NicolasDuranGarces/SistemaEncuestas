    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int tipoPregunta;
        String enunciado = txtEnunciado.getText();
        if (RdbNumerico.isSelected()) {
            if (enunciado.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe Agregar Opciones");
            } else {
                tipoPregunta = 7;
                try {
                    if (controladorAgrePreguntas.crear(tipoPregunta, idSupCategoria, enunciado, false)) {
                        JOptionPane.showMessageDialog(null, "Registro Exitoso");
                        Limpiar();
                        this.dispose();
                    }
                } catch (DniUnicoExcepcion | ConexionException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        } else if (RdbNominal.isSelected()) {
            if (enunciado.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe Agregar Opciones");
            } else {
                tipoPregunta = 8;
                try {
                    if (controladorAgrePreguntas.crear(tipoPregunta, idSupCategoria, enunciado, false)) {
                        JOptionPane.showMessageDialog(null, "Registro Exitoso");
                        Limpiar();
                        this.dispose();
                    }
                } catch (DniUnicoExcepcion | ConexionException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por Favor Elija Su Tipo De Pregunta");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (RdbNumerico.isSelected()) {
            if (txtEnunciado.getText().isEmpty() || txtOpcionNumerica.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos los campos son requeridos");
            } else {
                Opcion op = new Opcion(0, x + 1, txtOpcionNumerica.getText(), false);
                txtOpcionNumerica.setText("");
                tblNumerica.setModel(controladorAgrePreguntas.cargarTabla(op, tblNumerica));
                x++;
            }
        } else if (RdbNominal.isSelected()) {
            if (txtEnunciado.getText().isEmpty() || txtOpcionNominal.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos los campos son requeridos");
            } else {
                Opcion op = new Opcion(0, x + 1, txtOpcionNominal.getText(), false);
                txtOpcionNominal.setText("");
                tblNominal.setModel(controladorAgrePreguntas.cargarTabla(op, tblNominal));
                x++;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por Favor Elija Su Tipo De Pregunta");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

