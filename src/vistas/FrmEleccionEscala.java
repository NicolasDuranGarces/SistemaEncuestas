    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int indice = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el Id de la opcion que desea Eliminar"));
        controladorAgrePreguntas.eliminarOpcion(indice);
        if (RdbNumerico.isSelected()) {
            tblNumerica.setModel(controladorAgrePreguntas.cargarTabla(null, tblNumerica));
        } else if (RdbNominal.isSelected()) {
            tblNominal.setModel(controladorAgrePreguntas.cargarTabla(null, tblNominal));
        }
        JOptionPane.showMessageDialog(null, "Opcion Descartada");
    }//GEN-LAST:event_btnEliminarActionPerformed

